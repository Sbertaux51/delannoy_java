package cours.chap05;

// Exemple de boucle 'for' exécutée tant que la condition est vraie
// La condition est examinée avant la boucle
public class For1 {
	public static void main (String args[]) {
		int i;
		for (i=1 ; i<=5 ; i++) { 
			System.out.print("Bonjour ");
			System.out.println(i + " fois");
		}	
	}
}
